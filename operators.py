# Operatörler değişkenler ve değerler üzerinde işlem yapmak için kullanılır.

# A)Aritmetik Operatörler

# toplama öperatörü
print(10 + 5)

# çıkarma öperatörü
print(10 - 5)

# çarpma öperatörü
print(10 * 5)

# bölme öperatörü
print(10 / 5)

# mod alma öperatörü
print(10 % 5)


# B) Atama Operatörü (=) ile yapılır.
a = 10
print(a)

# C) Karşılaştırma Operatörleri

# eşittir operatörü
a = 10
if a == 10:
    print("a 10'a eşittir")

# eşit değildir operatörü
a = 10
if a != 20:
    print("a 20'ye eşit değildir.")

# büyüktür operatörü
a = 10
if a > 5:
    print("a 5'ten büyüktür.")

# küçüktür operatörü
a = 10
if a > 15:
    print("a 15'ten küçüktür.")

# büyük eşit operatörü
a = 10
if a >= 5:
    print("a 5'ten büyük yada eşittir.")

# küçük eşit operatörü
a = 10
if a >= 5:
    print("a 10'dan küçük yada eşittir.")
